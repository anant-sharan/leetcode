class Solution {
    public void nextPermutation(int[] nums) {
        int len=nums.length,i,c=0,t=0,l=0,r=len-1,p=0,j;
        for(i=len-1;i>0;i--)
        {
            if(nums[i]>nums[i-1])
            {
                p=i-1;
                l=i;
                ++c;
                break;
            }
        }
        if(c==0)
        {
            Arrays.sort(nums);
        }
        else
        {
            for(j=len-1;j>=0;j--)
            {
                if(nums[j]>nums[p])
                {
                    t=nums[j];
                    nums[j]=nums[p];
                    nums[p]=t;
                    break;
                }
            }
            while(l<=r)
            {
                t=nums[l];
                nums[l]=nums[r];
                nums[r]=t;
                ++l;
                --r;
            }
        }
    }
}